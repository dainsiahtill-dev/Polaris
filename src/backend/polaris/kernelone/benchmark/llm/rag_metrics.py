def _tokenize(text: str) -> set[str]:
    """Tokenize text into word set."""
    words = re.findall(r"\w+", text.lower())
    return set(words)


def calculate_recall(
    retrieved: tuple[str, ...],
    reference: tuple[str, ...],
) -> float:
    """Calculate recall (how much reference is covered by retrieved).

    Args:
        retrieved: Retrieved context chunks.
        reference: Reference context chunks.

    Returns:
        Recall score (0.0-1.0).
    """
    if not reference:
        return 1.0 if not retrieved else 0.0

    ref_tokens = set()
    for chunk in reference:
        ref_tokens.update(_tokenize(chunk))

    ret_tokens = set()
    for chunk in retrieved:
        ret_tokens.update(_tokenize(chunk))

    if not ref_tokens:
        return 1.0

    overlap = len(ref_tokens & ret_tokens)
    return overlap / len(ref_tokens)


def calculate_precision(
    retrieved: tuple[str, ...],
    reference: tuple[str, ...],
) -> float:
    """Calculate precision (how much retrieved is relevant).

    Args:
        retrieved: Retrieved context chunks.
        reference: Reference context chunks.

    Returns:
        Precision score (0.0-1.0).
    """
    if not retrieved:
        return 0.0

    ref_tokens = set()
    for chunk in reference:
        ref_tokens.update(_tokenize(chunk))

    ret_tokens = set()
    for chunk in retrieved:
        ret_tokens.update(_tokenize(chunk))

    if not ret_tokens:
        return 0.0

    overlap = len(ref_tokens & ret_tokens)
    return overlap / len(ret_tokens)


def calculate_rouge_l(
    candidate: str,
    reference: str,
) -> float:
    """Calculate ROUGE-L (Longest Common Subsequence) score.

    Args:
        candidate: The candidate text.
        reference: The reference text.

    Returns:
        ROUGE-L score (0.0-1.0).
    """
    if not candidate or not reference:
        return 0.0

    candidate_words = candidate.split()
    reference_words = reference.split()

    # Build LCS matrix
    m = len(candidate_words)
    n = len(reference_words)

    # Optimize for large texts by limiting size
    if m > 100 or n > 100:
        # For large texts, use a simpler approach
        cand_set = set(candidate_words)
        ref_set = set(reference_words)
        lcs_length = len(cand_set & ref_set)
        return lcs_length / max(len(ref_set), 1)

    # Standard LCS DP
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if candidate_words[i - 1] == reference_words[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
            else:
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])

    lcs_length = dp[m][n]

    # Calculate ROUGE-L precision and recall
    precision = lcs_length / m if m > 0 else 0.0
    recall = lcs_length / n if n > 0 else 0.0

    if precision + recall == 0:
        return 0.0

    return 2 * precision * recall / (precision + recall)


def calculate_context_relevance(
    query: str,
    context: tuple[str, ...],
) -> float:
    """Calculate relevance of context to query.

    Args:
        query: The user query.
        context: Retrieved context chunks.

    Returns:
        Relevance score (0.0-1.0).
    """
    if not context:
        return 0.0

    query_tokens = _tokenize(query)

    relevances = []
    for chunk in context:
        chunk_tokens = _tokenize(chunk)
        if not query_tokens:
            relevances.append(0.0)
            continue

        # Jaccard similarity
        overlap = len(query_tokens & chunk_tokens)
        relevance = overlap / len(query_tokens)
        relevances.append(relevance)

    return sum(relevances) / len(relevances) if relevances else 0.0


def calculate_answer_relevance(
    query: str,
    answer: str,
) -> float:
    """Calculate relevance of answer to query.

    Args:
        query: The user query.
        answer: The generated answer.

    Returns:
        Relevance score (0.0-1.0).
    """
    if not answer:
        return 0.0

    query_tokens = _tokenize(query)
    answer_tokens = _tokenize(answer)

    if not query_tokens:
        return 0.5

    # Calculate coverage
    overlap = len(query_tokens & answer_tokens)
    return overlap / len(query_tokens)


def calculate_hallucination_rate(
    answer: str,
    context: tuple[str, ...],
) -> float:
    """Calculate hallucination rate based on context coverage.

    Args:
        answer: The generated answer.
        context: Retrieved context chunks.

    Returns:
        Hallucination rate (0.0-1.0, lower is better).
    """
    if not answer:
        return 1.0

    if not context:
        return 0.5  # Neutral when no context

    context_tokens: set[str] = set()
    for chunk in context:
        context_tokens.update(_tokenize(chunk))

    answer_tokens = _tokenize(answer)

    if not answer_tokens:
        return 0.0

    # Hallucinated tokens are those not in context
    hallucinated = len(answer_tokens - context_tokens)
    total = len(answer_tokens)

    return hallucinated / total if total > 0 else 0.0


def calculate_missing_information(
    answer: str,
    reference: str,
) -> float:
    """Calculate missing information compared to reference.

    Args:
        answer: The generated answer.
        reference: The reference answer.

    Returns:
        Missing information rate (0.0-1.0).
    """
    if not reference:
        return 0.0

    if not answer:
        return 1.0

    ref_tokens = _tokenize(reference)
    answer_tokens = _tokenize(answer)

    if not ref_tokens:
        return 0.0

    # Information in reference not covered by answer
    missing = len(ref_tokens - answer_tokens)
    return missing / len(ref_tokens)


