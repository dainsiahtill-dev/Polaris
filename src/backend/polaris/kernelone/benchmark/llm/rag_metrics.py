@dataclass(frozen=True, kw_only=True)
class RAGTestCase:
    """Test case for RAG evaluation.

    Attributes:
        case_id: Unique identifier for this test case.
        query: The user's query.
        retrieved_context: The context retrieved by the system.
        reference_context: The ground truth context that should be retrieved.
        reference_answer: The ground truth answer.
        generated_answer: The answer generated by the RAG system.
        relevant_docs: Optional IDs of relevant documents.
        retrieved_doc_ids: Optional IDs of retrieved documents.
    """

    case_id: str
    query: str
    retrieved_context: tuple[str, ...] = field(default_factory=tuple)
    reference_context: tuple[str, ...] = field(default_factory=tuple)
    reference_answer: str = ""
    generated_answer: str = ""
    relevant_docs: tuple[str, ...] = field(default_factory=tuple)
    retrieved_doc_ids: tuple[str, ...] = field(default_factory=tuple)

    def __post_init__(self) -> None:
        if not self.case_id:
            raise ValueError("case_id is required")
        if not self.query:
            raise ValueError("query is required")

    def to_dict(self) -> dict[str, Any]:
        """Convert to dictionary."""
        return {
            "case_id": self.case_id,
            "query": self.query,
            "retrieved_context": list(self.retrieved_context),
            "reference_context": list(self.reference_context),
            "reference_answer": self.reference_answer,
            "generated_answer": self.generated_answer,
            "relevant_docs": list(self.relevant_docs),
            "retrieved_doc_ids": list(self.retrieved_doc_ids),
        }


@dataclass(frozen=True, kw_only=True)
class RAGMetrics:
    """Comprehensive RAG evaluation metrics.

    Attributes:
        recall: Context recall score (0.0-1.0).
        precision: Context precision score (0.0-1.0).
        f1: F1 score combining recall and precision.
        answer_accuracy: How accurate the generated answer is (0.0-1.0).
        context_relevance: Relevance of retrieved context to query (0.0-1.0).
        answer_relevance: Relevance of answer to query (0.0-1.0).
        hallucination_rate: Rate of hallucinated content (0.0-1.0).
        noise_sensitivity: How much noise in context affects answer (0.0-1.0).
        missing_information: Information missing from answer (0.0-1.0).
        rouge_l: ROUGE-L score if reference answer provided.
        bert_score: BERT score if available.
    """

    recall: float = 0.0
    precision: float = 0.0
    f1: float = 0.0
    answer_accuracy: float = 0.0
    context_relevance: float = 0.0
    answer_relevance: float = 0.0
    hallucination_rate: float = 0.0
    noise_sensitivity: float = 0.0
    missing_information: float = 0.0
    rouge_l: float = 0.0
    bert_score: float = 0.0

    def __post_init__(self) -> None:
        # Clamp all scores to [0.0, 1.0]
        for field_name in [
            "recall",
            "precision",
            "f1",
            "answer_accuracy",
            "context_relevance",
            "answer_relevance",
            "hallucination_rate",
            "noise_sensitivity",
            "missing_information",
            "rouge_l",
            "bert_score",
        ]:
            value = getattr(self, field_name)
            clamped = max(0.0, min(1.0, value))
            object.__setattr__(self, field_name, clamped)

    @property
    def overall_score(self) -> float:
        """Calculate overall RAG quality score."""
        return (
            self.recall * 0.25
            + self.precision * 0.15
            + self.answer_accuracy * 0.25
            + self.context_relevance * 0.15
            + self.answer_relevance * 0.10
            + (1.0 - self.hallucination_rate) * 0.10
        )

    def to_dict(self) -> dict[str, Any]:
        """Convert to dictionary."""
        return {
            "recall": round(self.recall, 4),
            "precision": round(self.precision, 4),
            "f1": round(self.f1, 4),
            "answer_accuracy": round(self.answer_accuracy, 4),
            "context_relevance": round(self.context_relevance, 4),
            "answer_relevance": round(self.answer_relevance, 4),
            "hallucination_rate": round(self.hallucination_rate, 4),
            "noise_sensitivity": round(self.noise_sensitivity, 4),
            "missing_information": round(self.missing_information, 4),
            "rouge_l": round(self.rouge_l, 4),
            "bert_score": round(self.bert_score, 4),
            "overall_score": round(self.overall_score, 4),
        }


