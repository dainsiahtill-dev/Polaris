class RAGEvaluator:
    """Comprehensive RAG evaluation engine.

    This evaluator calculates multiple metrics for RAG systems
    including recall, precision, answer quality, and hallucination.
    """

    def __init__(
        self,
        use_rouge: bool = True,
        use_bert_score: bool = False,
    ) -> None:
        """Initialize the evaluator.

        Args:
            use_rouge: Whether to calculate ROUGE scores.
            use_bert_score: Whether to calculate BERT scores (requires transformers).
        """
        self._use_rouge = use_rouge
        self._use_bert_score = use_bert_score

    def evaluate(self, test_case: RAGTestCase) -> RAGMetrics:
        """Evaluate a RAG test case.

        Args:
            test_case: The test case to evaluate.

        Returns:
            RAGMetrics with all calculated scores.
        """
        # Calculate recall and precision
        recall = calculate_recall(
            test_case.retrieved_context,
            test_case.reference_context,
        )
        precision = calculate_precision(
            test_case.retrieved_context,
            test_case.reference_context,
        )

        # Calculate F1
        f1 = 0.0 if recall + precision == 0 else 2 * recall * precision / (recall + precision)

        # Calculate answer accuracy
        answer_accuracy = 0.0
        if test_case.reference_answer and test_case.generated_answer:
            answer_accuracy = calculate_rouge_l(
                test_case.generated_answer,
                test_case.reference_answer,
            )

        # Calculate context relevance
        context_relevance = calculate_context_relevance(
            test_case.query,
            test_case.retrieved_context,
        )

        # Calculate answer relevance
        answer_relevance = calculate_answer_relevance(
            test_case.query,
            test_case.generated_answer,
        )

        # Calculate hallucination rate
        hallucination_rate = calculate_hallucination_rate(
            test_case.generated_answer,
            test_case.reference_context,
        )

        # Calculate missing information
        missing_info = calculate_missing_information(
            test_case.generated_answer,
            test_case.reference_answer,
        )

        # Calculate ROUGE-L
        rouge_l = 0.0
        if self._use_rouge and test_case.reference_answer and test_case.generated_answer:
            rouge_l = calculate_rouge_l(
                test_case.generated_answer,
                test_case.reference_answer,
            )

        # BERT score placeholder
        bert_score = 0.0
        if self._use_bert_score:
            bert_score = self._calculate_bert_score(
                test_case.generated_answer,
                test_case.reference_answer,
            )

        # Noise sensitivity (based on precision and context relevance)
        noise_sensitivity = 1.0 - precision

        return RAGMetrics(
            recall=recall,
            precision=precision,
            f1=f1,
            answer_accuracy=answer_accuracy,
            context_relevance=context_relevance,
            answer_relevance=answer_relevance,
            hallucination_rate=hallucination_rate,
            noise_sensitivity=noise_sensitivity,
            missing_information=missing_info,
            rouge_l=rouge_l,
            bert_score=bert_score,
        )

    def _calculate_bert_score(
        self,
        candidate: str,
        reference: str,
    ) -> float:
        """Calculate BERT score (placeholder - requires transformers)."""
        # This is a placeholder. Real implementation would use:
        # from bert_score import score
        # _, _, f1 = score([candidate], [reference], lang="en")
        # return float(f1[0])
        return 0.0

    def evaluate_batch(
        self,
        test_cases: list[RAGTestCase],
    ) -> tuple[dict[str, float], list[RAGMetrics]]:
        """Evaluate a batch of test cases.

        Args:
            test_cases: List of test cases to evaluate.

        Returns:
            Tuple of (aggregated metrics, individual metrics).
        """
        all_metrics = [self.evaluate(case) for case in test_cases]

        # Aggregate metrics
        aggregated = {
            "avg_recall": sum(m.recall for m in all_metrics) / len(all_metrics) if all_metrics else 0.0,
            "avg_precision": sum(m.precision for m in all_metrics) / len(all_metrics) if all_metrics else 0.0,
            "avg_f1": sum(m.f1 for m in all_metrics) / len(all_metrics) if all_metrics else 0.0,
            "avg_answer_accuracy": sum(m.answer_accuracy for m in all_metrics) / len(all_metrics)
            if all_metrics
            else 0.0,
            "avg_context_relevance": sum(m.context_relevance for m in all_metrics) / len(all_metrics)
            if all_metrics
            else 0.0,
            "avg_answer_relevance": sum(m.answer_relevance for m in all_metrics) / len(all_metrics)
            if all_metrics
            else 0.0,
            "avg_hallucination_rate": sum(m.hallucination_rate for m in all_metrics) / len(all_metrics)
            if all_metrics
            else 0.0,
            "avg_overall_score": sum(m.overall_score for m in all_metrics) / len(all_metrics) if all_metrics else 0.0,
        }

        return aggregated, all_metrics


