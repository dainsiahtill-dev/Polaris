@dataclass
class RetrievalMetrics:
    """Metrics for document retrieval evaluation.

    Attributes:
        hit_rate_at_k: Fraction of queries with at least one relevant doc in top-k.
        mrr: Mean Reciprocal Rank.
        ndcg: Normalized Discounted Cumulative Gain.
        average_precision: Mean Average Precision.
    """

    hit_rate_at_1: float = 0.0
    hit_rate_at_5: float = 0.0
    hit_rate_at_10: float = 0.0
    mrr: float = 0.0
    ndcg: float = 0.0
    average_precision: float = 0.0

    def to_dict(self) -> dict[str, Any]:
        """Convert to dictionary."""
        return {
            "hit_rate_at_1": round(self.hit_rate_at_1, 4),
            "hit_rate_at_5": round(self.hit_rate_at_5, 4),
            "hit_rate_at_10": round(self.hit_rate_at_10, 4),
            "mrr": round(self.mrr, 4),
            "ndcg": round(self.ndcg, 4),
            "average_precision": round(self.average_precision, 4),
        }


class RetrievalEvaluator:
    """Evaluator for document retrieval metrics.

    This evaluator calculates retrieval-specific metrics
    based on document IDs rather than text content.
    """

    def evaluate_retrieval(
        self,
        queries: list[str],
        retrieved_doc_ids: list[list[str]],
        relevant_doc_ids: list[set[str]],
        k_values: tuple[int, ...] = (1, 5, 10),
    ) -> RetrievalMetrics:
        """Evaluate document retrieval performance.

        Args:
            queries: List of queries.
            retrieved_doc_ids: For each query, list of retrieved doc IDs.
            relevant_doc_ids: For each query, set of relevant doc IDs.
            k_values: K values for hit rate calculation.

        Returns:
            RetrievalMetrics with all calculated scores.
        """
        n = len(queries)
        if n == 0:
            return RetrievalMetrics()

        hit_rates: dict[int, list[float]] = {k: [] for k in k_values}
        reciprocal_ranks: list[float] = []
        average_precisions: list[float] = []
        ndcg_scores: list[float] = []

        for _i, (retrieved, relevant) in enumerate(zip(retrieved_doc_ids, relevant_doc_ids, strict=True)):
            if not relevant:
                continue

            # Hit rate at k
            for k in k_values:
                top_k = set(retrieved[:k])
                hits = len(top_k & relevant)
                hit_rates[k].append(1.0 if hits > 0 else 0.0)

            # Mean Reciprocal Rank
            rr = 0.0
            for rank, doc_id in enumerate(retrieved, 1):
                if doc_id in relevant:
                    rr = 1.0 / rank
                    break
            reciprocal_ranks.append(rr)

            # Average Precision
            precisions = []
            num_relevant_so_far = 0
            for rank, doc_id in enumerate(retrieved, 1):
                if doc_id in relevant:
                    num_relevant_so_far += 1
                    precisions.append(num_relevant_so_far / rank)
            avg_precision = sum(precisions) / len(relevant) if relevant else 0.0
            average_precisions.append(avg_precision)

            # NDCG
            dcg = 0.0
            for rank, doc_id in enumerate(retrieved, 1):
                if doc_id in relevant:
                    dcg += 1.0 / (rank**0.5)  # Using sqrt for discount

            # IDCG (ideal DCG)
            idcg = sum(1.0 / (r**0.5) for r in range(1, len(relevant) + 1))
            ndcg = dcg / idcg if idcg > 0 else 0.0
            ndcg_scores.append(ndcg)

        return RetrievalMetrics(
            hit_rate_at_1=sum(hit_rates.get(1, [0.0])) / len(hit_rates.get(1, [1.0])) if hit_rates.get(1) else 0.0,
            hit_rate_at_5=sum(hit_rates.get(5, [0.0])) / len(hit_rates.get(5, [1.0])) if hit_rates.get(5) else 0.0,
            hit_rate_at_10=sum(hit_rates.get(10, [0.0])) / len(hit_rates.get(10, [1.0])) if hit_rates.get(10) else 0.0,
            mrr=sum(reciprocal_ranks) / len(reciprocal_ranks) if reciprocal_ranks else 0.0,
            ndcg=sum(ndcg_scores) / len(ndcg_scores) if ndcg_scores else 0.0,
            average_precision=sum(average_precisions) / len(average_precisions) if average_precisions else 0.0,
        )
