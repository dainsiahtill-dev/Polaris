"""RAG Context Recall and Relevance Evaluation Framework.

This module provides comprehensive RAG (Retrieval-Augmented Generation)
evaluation metrics including context recall, answer accuracy, and
retrieval quality assessment.

Example
-------
    from polaris.kernelone.benchmark.llm.rag_metrics import (
        RAGTestCase,
        RAGMetrics,
        RAGEvaluator,
        calculate_recall,
        calculate_precision,
    )

    test_case = RAGTestCase(
        case_id="config_lookup",
        query="What is the database host?",
        retrieved_context=["db_host=localhost", "port=5432"],
        reference_context=["db_host=localhost"],
        reference_answer="The database host is localhost",
        generated_answer="The database host is localhost",
    )

    evaluator = RAGEvaluator()
    metrics = evaluator.evaluate(test_case)
"""

