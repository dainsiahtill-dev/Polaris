def list_holographic_cases() -> tuple[HolographicCase, ...]:
    """Return all benchmark cases in canonical order."""
    return HOLOGRAPHIC_CASES


def case_ids() -> tuple[str, ...]:
    """Return all case IDs."""
    return tuple(case.case_id for case in HOLOGRAPHIC_CASES)


def ready_case_ids() -> tuple[str, ...]:
    """Return IDs for executable cases."""
    return tuple(case.case_id for case in HOLOGRAPHIC_CASES if case.is_ready)
