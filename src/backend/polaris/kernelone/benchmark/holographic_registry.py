def _case(
    *,
    case_id: str,
    subsystem: str,
    title: str,
    target_path: str,
    summary: str,
    readiness: CaseReadiness,
    thresholds: dict[str, object],
    blocker: str = "",
    warmup_rounds: int = 3,
    min_samples: int = 100,
) -> HolographicCase:
    return HolographicCase(
        case_id=case_id,
        subsystem=subsystem,
        title=title,
        target_path=target_path,
        summary=summary,
        readiness=readiness,
        thresholds=thresholds,
        blocker=blocker,
        warmup_rounds=warmup_rounds,
        min_samples=min_samples,
    )


